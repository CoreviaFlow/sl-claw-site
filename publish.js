// ── главный прогон ──
const niceBySlug = {}; D.niches.forEach(n=>niceBySlug[n.slug]=n);
let published = 0;
const touchedNiches = new Set(); // `${slug}|${lang}`
const newlyByNicheLang = {}; // для related/индексов

// 1) публикуем то, что должно
outer:
for(const slug in plan.niches){
  const n = niceBySlug[slug]; if(!n) continue;
  for(const post of plan.niches[slug].posts){
    if(post.status === 'published') continue;
    const due = post.publishAt || (post.publish + 'T23:59:59Z'); // нет времени → конец дня
    if(due > NOW) continue;
    if(published >= LIMIT) break outer;
    const lang = post.lang;
    const f = F(n, lang);
    const titles = postTitles(f.name, lang);
    let themeIdx = titles.indexOf(post.title);
    if(themeIdx < 0) themeIdx = Math.floor(rng(post.title)()*25);
    // slug поста
    let ps = slugify(post.title.replace(`«${f.name}»`,'').replace(f.name,'')).replace(/^-+/,'') || ('post-'+(themeIdx+1));
    post.slug = ps; post.status='published'; post.publishedAt = new Date().toISOString();
    published++;
    touchedNiches.add(slug+'|'+lang);
    (newlyByNicheLang[slug+'|'+lang] = newlyByNicheLang[slug+'|'+lang]||[]).push({post,themeIdx,n,lang});
  }
}

// 2) собрать ВСЕ опубликованные посты; всегда перестраивать индексы/хабы (самолечение),
//    статьи писать только новые (за этот прогон)
const globalItems = [];
for(const slug in plan.niches){
  const n = niceBySlug[slug]; if(!n) continue;
  for(const lang of ['ru','uk']){
    const pub = plan.niches[slug].posts.filter(p=>p.status==='published' && p.lang===lang && p.slug)
      .sort((a,b)=> (b.publishedAt||b.publish).localeCompare(a.publishedAt||a.publish));
    if(!pub.length) continue;
    // глоб.лента
    pub.forEach(p=>globalItems.push({...p, nslug:slug, niche:F(n,lang).name, lang}));
    const key = slug+'|'+lang;
    // какие статьи писать: новые этого прогона + (при PUBLISH_REBUILD=1) все опубликованные
    const writeSlugs = new Set();
    if(touchedNiches.has(key)) for(const {post} of newlyByNicheLang[key]) writeSlugs.add(post.slug);
    if(REBUILD) for(const p of pub) writeSlugs.add(p.slug);
    if(writeSlugs.size){
      const f = F(n, lang); const titles = postTitles(f.name, lang);
      // карта постов другого языка по теме → hreflang ru↔uk
      const oLang = lang==='uk'?'ru':'uk';
      const oTitles = postTitles(F(n,oLang).name, oLang);
      const oByTheme = {};
      for(const p of plan.niches[slug].posts){ if(p.status==='published'&&p.lang===oLang&&p.slug){ const ti=oTitles.indexOf(p.title); if(ti>=0) oByTheme[ti]=p.slug; } }
      for(const post of pub){
        if(!writeSlugs.has(post.slug)) continue;
        let themeIdx = titles.indexOf(post.title); if(themeIdx<0) themeIdx = Math.floor(rng(post.title)()*25);
        const related = pub.filter(p=>p.slug!==post.slug).slice(0,5);
        const altUrl = oByTheme[themeIdx]!=null ? postUrl(slug, oLang, oByTheme[themeIdx]) : '';
        const dir = path.join(ROOT, DIR(lang), slug, 'blog', post.slug);
        fs.mkdirSync(dir,{recursive:true});
        const al = (lang==='uk' ? (D.archetypes_uk||{}) : (D.archetypes||{}))[n.archetype] || n.archetype;
        fs.writeFileSync(path.join(dir,'cover.svg'), coverSVG({
          eyebrow: lang==='uk' ? '// блог · автоматизація продажів' : '// блог · автоматизация продаж',
          title: post.title, sub: f.name, archLabel: al, archetype: n.archetype,
          term: 'docker compose up -d',
        }));
        fs.writeFileSync(path.join(dir,'index.html'), renderArticle(n, lang, post, themeIdx, pretty(post.publishedAt||post.publish,lang), related, altUrl));
      }
    }
    // индекс блога ниши + блок на хабе — ВСЕГДА (восстанавливает связь, если seo-build сбросил хаб)
    const bdir = path.join(ROOT, DIR(lang), slug, 'blog');
    fs.mkdirSync(bdir,{recursive:true});
    fs.writeFileSync(path.join(bdir,'index.html'), renderNicheBlogIndex(n, lang, pub));
    updateNicheHubBlogList(n, lang, pub);
  }
}

